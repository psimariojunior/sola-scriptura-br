export function locaisPorTestamento(t: 'AT' | 'NT' | 'ambos' | 'todos'): LocalBiblico[] {
  if (t === 'todos') return locaisBiblicos;
  return locaisBiblicos.filter((l) => l.periodo === t || l.periodo === 'ambos');
}

export function locaisPorCategoria(c: string): LocalBiblico[] {
  if (c === 'todos') return locaisBiblicos;
  return locaisBiblicos.filter((l) => l.categoria === c);
}

export function buscarLocal(q: string): LocalBiblico[] {
  const term = q.toLowerCase();
  return locaisBiblicos.filter(
    (l) =>
      l.nome.toLowerCase().includes(term) ||
      l.nomeEn.toLowerCase().includes(term) ||
      (l.nomeHebraico && l.nomeHebraico.toLowerCase().includes(term)) ||
      (l.nomeGriego && l.nomeGriego.toLowerCase().includes(term)) ||
      l.descricao.toLowerCase().includes(term)
  );
}

export function rotasPorPeriodo(p: 'AT' | 'NT' | 'todos'): RotaBiblica[] {
  if (p === 'todos') return rotasBiblicas;
  return rotasBiblicas.filter((r) => r.periodo === p);
}
