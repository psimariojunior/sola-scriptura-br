export function getIntroducaoLivroCompleta(slug: string): IntroducaoLivro | undefined {
  return introducoesCompletas.find((i) => i.livro === slug);
}

export function getIntroducoesPorTestamento(testamento: 'AT' | 'NT'): IntroducaoLivro[] {
  return introducoesCompletas.filter((i) => i.testamento === testamento);
}

export function getIntroducoesPorCategoria(categoria: string): IntroducaoLivro[] {
  return introducoesCompletas.filter((i) => i.categoria === categoria);
}

export function getPesquisaIntroducoes(termo: string): IntroducaoLivro[] {
  const termoLower = termo.toLowerCase();
  return introducoesCompletas.filter((i) =>
    i.nome.toLowerCase().includes(termoLower) ||
    i.livro.toLowerCase().includes(termoLower) ||
    i.temaPrincipal.toLowerCase().includes(termoLower) ||
    i.resumo.toLowerCase().includes(termoLower) ||
    i.categoria.toLowerCase().includes(termoLower)
  );
}
