export function locaisPorTestamento(t: 'AT' | 'NT' | 'ambos' | 'todos'): LocalBiblico[] {
  if (t === 'todos') return locaisBiblicos;
  return locaisBiblicos.filter((l) => l.testamento === t || l.testamento === 'ambos');
}

export function buscarLocal(q: string): LocalBiblico[] {
  const term = q.toLowerCase();
  return locaisBiblicos.filter(
    (l) =>
      l.nome.toLowerCase().includes(term) ||
      l.nomeEn.toLowerCase().includes(term) ||
      l.descricao.toLowerCase().includes(term)
  );
}
