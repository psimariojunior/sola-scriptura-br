export function getPalavraPorStrong(strong: string): PalavraAramaica | undefined {
  return palavrasAramaicas.find(p => p.strong === strong);
}

export function getPalavrasPorOcorrencia(referencia: string): PalavraAramaica[] {
  const refNormalizada = referencia.toLowerCase().trim();
  return palavrasAramaicas.filter(p => p.ocorrencias.includes(refNormalizada));
}

export function getPalavrasPorDefinicao(termo: string): PalavraAramaica[] {
  const termoNormalizado = termo.toLowerCase().trim();
  return palavrasAramaicas.filter(p => p.definicao.toLowerCase().includes(termoNormalizado));
}
