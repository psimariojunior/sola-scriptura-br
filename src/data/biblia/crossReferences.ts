function buildMap(data: CrossReference[]): Record<string, CrossReference[]> {
  const map: Record<string, CrossReference[]> = {};
  for (const ref of data) {
    const fromKey = ref.from.toLowerCase().replace(/\s+/g, '/').replace(/:/g, '/');
    if (!map[fromKey]) map[fromKey] = [];
    map[fromKey].push(ref);
  }
  return map;
}

export const crossReferencesMap: Record<string, CrossReference[]> = buildMap(refs);

export function getCrossReferencesByVerse(
  book: string,
  chapter: number,
  verse: number
): CrossReference[] {
  const key = `${book.toLowerCase()}/${chapter}/${verse}`;
  return crossReferencesMap[key] || [];
}

export function getCrossReferencesByType(
  book: string,
  chapter: number,
  verse: number,
  type: CrossReference['type']
): CrossReference[] {
  return getCrossReferencesByVerse(book, chapter, verse).filter((r) => r.type === type);
}

export function formatReference(ref: string): string {
  return ref.replace(/\//g, ' ');
}
