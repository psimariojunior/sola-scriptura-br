export function getTeologo(slug: string): Teologo | undefined {
  return todosTeologos.find((t) => t.slug === slug);
}

export function getTeologosPorPeriodo(periodo: string): Teologo[] {
  return todosTeologos.filter((t) => t.periodo === periodo);
}

export function getTeologosPorTradicao(tradicao: string): Teologo[] {
  const t = tradicao.toLowerCase();
  return todosTeologos.filter(
    (teologo) =>
      teologo.tradicao.toLowerCase().includes(t) ||
      teologo.escola?.toLowerCase().includes(t)
  );
}

export function listarTodosTeologos(): Teologo[] {
  return [...todosTeologos];
}
