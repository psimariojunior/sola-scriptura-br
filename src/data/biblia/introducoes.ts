export function getIntroducaoLivro(slug: string): IntroducaoLivro | undefined {
  return introducoes.find((i) => i.livro === slug);
}

export function getIntroducoesPorTestamento(testamento: 'AT' | 'NT'): IntroducaoLivro[] {
  const livrosAT = [
    'gn', 'ex', 'lv', 'nm', 'dt', 'js', 'jz', 'rt', '1sm', '2sm',
    '1rs', '2rs', '1cr', '2cr', 'ed', 'ne', 'et', 'jó', 'sl', 'pv',
    'ec', 'ct', 'is', 'jr', 'lm', 'ez', 'dn', 'os', 'jl', 'am',
    'ob', 'jn', 'mq', 'na', 'hc', 'sf', 'ag', 'zc', 'ml',
  ];
  const abreviacoes = testamento === 'AT' ? livrosAT : introducoes.filter((i) => !livrosAT.includes(i.livro)).map((i) => i.livro);
  return introducoes.filter((i) => abreviacoes.includes(i.livro));
}

export function getIntroducoesPorGenero(genero: string): IntroducaoLivro[] {
  return introducoes.filter((i) => i.genero === genero);
}
