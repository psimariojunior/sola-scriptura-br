export function getPersonagemPorSlug(slug: string): PersonagemAvancado | undefined {
  return PERSONAGENS_AVANCADOS.find(p => p.slug === slug);
}

export function getPersonagensPorTestamento(testamento: 'AT' | 'NT'): PersonagemAvancado[] {
  return PERSONAGENS_AVANCADOS.filter(p => p.testamento === testamento);
}

export function getPersonagensPorPeriodo(periodo: string): PersonagemAvancado[] {
  const periodoNormalizado = periodo.toLowerCase().trim();
  return PERSONAGENS_AVANCADOS.filter(p =>
    p.periodos?.some(per => per.toLowerCase().includes(periodoNormalizado))
  );
}

export function getPersonagensPorDoutrina(doutrina: string): PersonagemAvancado[] {
  const doutrinaNormalizada = doutrina.toLowerCase().trim();
  return PERSONAGENS_AVANCADOS.filter(p =>
    p.doutrinas?.some(d => d.toLowerCase().includes(doutrinaNormalizada))
  );
}

export function getPersonagensPorReferencia(ref: string): PersonagemAvancado[] {
  const refNormalizada = ref.toLowerCase().trim();
  return PERSONAGENS_AVANCADOS.filter(p =>
    p.referencias.some(r => r.toLowerCase().includes(refNormalizada))
  );
}

export function getPersonagensContemporaneos(slug: string): PersonagemAvancado[] {
  const personagem = getPersonagemPorSlug(slug);
  if (!personagem?.contemporaneos) return [];
  return PERSONAGENS_AVANCADOS.filter(p =>
    personagem.contemporaneos!.some(c => c.toLowerCase() === p.slug.toLowerCase())
  );
}

export function getPersonagensPorTipologia(tipologia: string): PersonagemAvancado[] {
  const tipologiaNormalizada = tipologia.toLowerCase().trim();
  return PERSONAGENS_AVANCADOS.filter(p =>
    p.tipologia?.toLowerCase().includes(tipologiaNormalizada)
  );
}
