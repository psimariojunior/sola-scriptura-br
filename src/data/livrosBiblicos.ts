export function getLivroPorId(id: string): LivroBiblico | undefined {
  return livrosBiblicos.find(l => l.id === id);
}

export function getLivroPorNome(nome: string): LivroBiblico | undefined {
  const n = nome.toLowerCase();
  return livrosBiblicos.find(l => l.nome.toLowerCase() === n || l.nomeIngles.toLowerCase() === n);
}

export function getLivrosPorTestamento(testamento: 'AT' | 'NT'): LivroBiblico[] {
  return livrosBiblicos.filter(l => l.testamento === testamento);
}

export function getLivrosPorCategoria(categoria: string): LivroBiblico[] {
  return livrosBiblicos.filter(l => l.categoria === categoria);
}

export function getTotalVersiculos(): number {
  return livrosBiblicos.reduce((acc, l) => acc + l.versiculos, 0);
}
